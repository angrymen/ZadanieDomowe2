public class Trousers {


    int size;
    String fabric;
    String color;
    String forGender;
    String style;
    String producer;
    String currency;
    boolean promotion;
    double discount;
    double price;

    Trousers (int size, String fabric, String color, String forGender, String style, String producer, double price, String currency, boolean promotion, double discount) {

        this.size = size;
        this.fabric = fabric;
        this.color = color;
        this.forGender = forGender;
        this.style = style;
        this.producer = producer;
        this.discount = discount;
        this.promotion = promotion;
        this.price = price;
        this.currency = currency;



    }

}
